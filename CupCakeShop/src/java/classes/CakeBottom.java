package classes;

public class CakeBottom {
    
    private int bID;
    private String top;
    private int price;

    public CakeBottom(int bID, String top, int price) {
        this.bID = bID;
        this.top = top;
        this.price = price;
    }

    public int getbID() {
        return bID;
    }

    public String getTop() {
        return top;
    }

    public int getPrice() {
        return price;
    }
    
    
}
